
public class prefixpostfix {
    public static void main(String[] args) {
        /*Prefix operator */
    int prefixValue = 10;
    System.out.println("prefix value updates the value in thos line"+ ++prefixValue);
    System.out.println("It remain the same in the next line"+ prefixValue);


    /*Postfix Operator */

    int postfixValue = 20;
    System.out.println("postfix value updates the value in next line"+ postfixValue);
    System.out.println("It remain the same in the next line"+ postfixValue);
    }
    
}
